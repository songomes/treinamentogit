package treinamentojava.ordenacao.ricardo;

public class ItemVendaSimulator {

	private int numeroUa = 1;

	private int jan;

	public ItemVendaSimulator(int jan) {
		this.jan = jan;
	}

	public int getNumeroUa() {
		return numeroUa;
	}

	public void setNumeroUa(int numeroUa) {
		this.numeroUa = numeroUa;
	}

	public int getJan() {
		return jan;
	}

	public void setJan(int jan) {
		this.jan = jan;
	}

}